export const rooms = ['5C', '5D', '5E'] as const;

export function getRoomForGroup(group: number): typeof rooms[number] {
  // Group 3*k + 1 in 5C
  // Group 3*k + 2 in 5D
  // Group 3*k + 3 in 5E
  const mod = group % 3;
  if (mod === 1) return '5C';
  if (mod === 2) return '5D';
  return '5E';
}
